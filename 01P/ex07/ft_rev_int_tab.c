void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	start;
	int	end;
	int	temp;

	start = 0;
	end = size - 1;
	i = 0;
	while (start < end)
	{
		temp = tab[start];
		tab[start] = tab[end];
		tab[end] = temp;
		++start;
		--end;
	}
}
